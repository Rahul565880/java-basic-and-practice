public class ParseInt {
        public static void main(String[] args) {

            // Example 1
            String s1 = "123";
            int num1 = Integer.parseInt(s1);
            System.out.println("Value of s1: " + num1);

            // Example 2
            String s2 = "-45";
            int num2 = Integer.parseInt(s2);
            System.out.println("Value of s2: " + num2);

            // Example 3
            String s3 = "0";
            int num3 = Integer.parseInt(s3);
            System.out.println("Value of s3: " + num3);
        }
    }

